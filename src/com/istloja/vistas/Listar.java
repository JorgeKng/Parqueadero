    /**
     *
     * @param v
     */
    int contadorClick = 0;
    boolean ejecutar;

    @Override
    public void clickVehiculo(Vehiculo v) {
        ejecutar = true;
        //HILO DE PROGRMACIÓN
        new Thread(new Runnable() {
            @Override
            public void run() {
                while (ejecutar) {
                    try {
                        //Parar un segundo
                        Thread.sleep(400);
                        contadorClick = 0;
                        ejecutar = false;
                        System.out.println("Reinicia CLICK");
                    } catch (InterruptedException e) {
                        System.out.println(e.getMessage());
                    }
                }
            }
        }).start();

        contadorClick++;
        if (contadorClick == 2) {
            if (JOptionPane.showConfirmDialog(rootPane, "Se eliminará el item, ¿desea continuar?",
                    "Eliminar Producto", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    controladorVehiculo.EliminarVehiculo(v);
                    modeloTablaListar.setVehiculosbusq(controladorVehiculo.obtenerVehiculos());
                    modeloTablaListar.fireTableDataChanged();
            }
        }
    }

