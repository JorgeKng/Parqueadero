    private void btnBqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBqActionPerformed
        // TODO add your handling code here:
        Vehiculo vehiculo = new Vehiculo();
        vehiculo.setPlaca(txtPlacaB.getText());
        vehiculo.setPropietario(txtPropietarioB.getText());
        
        if (cbAuto.isSelected()) {
            if (cbMoto.isSelected()) {
                vehiculo.setTipo_Vehiculo("");;
            } else {
                vehiculo.setTipo_Vehiculo("Automovil");
            }
        } else if (cbMoto.isSelected()) {
            vehiculo.setTipo_Vehiculo("Motocicleta");
        }
        if(rbDentroParq.isSelected()){
            vehiculo.setEstado("Disponible");
        }

        if (rbFueraParq.isSelected()) {
           
            vehiculo.setEstado("No Disponible");
        }
       
        
        if (dateFechaB.getDate() != null) {
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            Date date = dateFechaB.getDate();
            String date1 = dateFormat.format(date);
            vehiculo.setHora_Entrada(date1);
        }
        
        
        System.out.println(controladorVehiculo.BuscarVehiculo(vehiculo));
        
        modeloTablaListar.setVehiculosbusq(controladorVehiculo.BuscarVehiculo(vehiculo));
        modeloTablaListar.fireTableDataChanged();
    }//GEN-LAST:event_btnBqActionPerformed

